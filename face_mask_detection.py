import os
import pandas as pd
from sklearn.model_selection import train_test_split
import matplotlib.pyplot as plt
from tensorflow.keras.preprocessing.image import ImageDataGenerator
from tensorflow.keras.applications import EfficientNetB3
import numpy as np
from tensorflow.keras.layers import Dense,Flatten , Dropout
from tensorflow.keras.models import Sequential
from tensorflow.keras.optimizers import Adam
from tensorflow.keras.callbacks import EarlyStopping
import warnings
warnings.filterwarnings("ignore")
from sklearn.metrics import classification_report,confusion_matrix
import seaborn as sns

"""# Function to create dataset includes paths and its labels"""

def create_data_set(path):
    images = []
    labels = []
    folders = os.listdir(path)
    for folder in folders:
        folder_path = os.path.join(path , folder)
        files = os.listdir(folder_path)
        for file in files:
            file_path = os.path.join(folder_path , file)
            images.append(file_path)
            labels.append(folder)
    data = pd.DataFrame({"file_paths" : images , "labels":labels})
    return data

data = create_data_set("/kaggle/input/face-mask-dataset/data")
data

"""# splitting the data into"""

train , dummy = train_test_split(data , test_size = 0.3 , random_state = 44 , stratify = data["labels"])

valid , test = train_test_split(dummy , test_size = 0.5 , random_state = 44 , stratify = dummy["labels"])

class_counts = train["labels"].value_counts()
class_counts

"""# some visulaization for the data"""

plt.figure(figsize=(10, 6))
