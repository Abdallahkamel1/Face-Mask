plt.title('Class Distribution')
plt.xticks(rotation=45)
plt.show()

plt.pie(class_counts.values , labels = class_counts.index , autopct = "%1.2f%%",explode = [0.1 , 0.1])

"""# effects that will be applied in training using data augmentation methodolgy"""

train_gen = ImageDataGenerator(
    rescale=1./255,           # Normalize pixel values
    rotation_range=10,        # Small rotations
    width_shift_range=0.1,    # Horizontal shift
    height_shift_range=0.1,   # Vertical shift
    zoom_range=0.1,           # Slight zoom in/out
    brightness_range=[0.8, 1.2],  # Adjust brightness
    horizontal_flip=True,     # Flip images horizontally
    fill_mode='nearest'       # Fill missing pixels
)

test_gen = ImageDataGenerator(rescale = 1./255)

"""**applying augmentation for the data**"""

training_data = train_gen.flow_from_dataframe(train,x_col = "file_paths" , y_col = "labels" , target_size = (256 , 256) , color_mode = "rgb" , class_mode = "categorical" , batch_size = 16 , shuffle = True)

validation_data = test_gen.flow_from_dataframe(valid,x_col = "file_paths" , y_col = "labels" , target_size = (256 , 256) , color_mode = "rgb" , class_mode = "categorical" , batch_size = 16 , shuffle = False)

testing_data  = test_gen.flow_from_dataframe(test,x_col = "file_paths" , y_col = "labels" , target_size = (256 , 256) , color_mode = "rgb" , class_mode = "categorical" , batch_size = 16 , shuffle = False)

"""# visualize a subset from the data #"""

images, labels = next(training_data)
