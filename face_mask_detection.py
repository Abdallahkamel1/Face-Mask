"""# creating the base model for extraction the features"""

base_model = EfficientNetB3(weights = "imagenet" , include_top=False,input_shape = (256 , 256 , 3) , pooling = "max")
base_model.trainable = True

"""# build the structure of the network"""

model = Sequential([
    base_model,
    Flatten(),
    Dense(128 , activation = "relu"),
    Dropout(0.3),
    Dense(64 , activation = "relu"),
    Dense(train["labels"].nunique() , activation = "softmax")
])

model.summary()

optimizer = Adam(learning_rate = 0.001)

model.compile(optimizer , loss = "categorical_crossentropy" , metrics = ["accuracy"])

early_stopping = EarlyStopping(monitor = "val_accuracy" , patience = 3 , restore_best_weights = True)

"""# training the model"""

history = model.fit(training_data , batch_size = 16, validation_data = validation_data , epochs = 20 , callbacks = [early_stopping])

"""# evaluating the model"""

testing_loss,testing_accuracy = model.evaluate(testing_data)
validation_loss , validation_accuracy = model.evaluate(validation_data)

print(f"testing_loss is {testing_loss}")
